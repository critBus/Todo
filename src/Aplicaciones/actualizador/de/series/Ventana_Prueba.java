    public void buscar(char a) {
        String A[] = Visual.leerJTextArea(TA);
        int ini = 0, end = -1;
        
        for (int i = 0; i < A.length; i++) {
//            System.out.println("A[i]="+A[i]);
            StringTokenizer St = new StringTokenizer(A[i]);
            if (St.hasMoreTokens() && St.nextToken().startsWith(a + "")) {
                end = ini + A[i].length()+(i!=0?1:0);
                break;
            }
            ini+=A[i].length()+(i!=0?1:0);
        }
        if(end!=-1){
        TA.setSelectionStart(ini);
        TA.setSelectionEnd(end);
        }else{
//            MetodosUtiles.pi
        }
    }

