    private Tiempo getInferior() {
        return extraerTiempo(Arrays.asList(T2).subList(1, 5).toArray(new JTextField[]{}));
    }

    private Tiempo getSuperior() {
        return extraerTiempo(Arrays.asList(T2).subList(5, T2.length).toArray(new JTextField[]{}));
    }

    private void vaciarTodo() {
        for (int i = 0; i < T2.length; i++) {
            T2[i].setText(i == 0 ? "1" : "0");
        }
        Ta.setText("");
        seguridad();
    }

    private void actualizar(int indice) {
        // System.out.println("indice="+indice);
        T2[0].setText((indice + 1) + "");
        for (int i = 1; i < T2.length; i++) {
            actualizarT(indice, i);
        }
        Ta.setText(S.S[indice].getDialogo());
    }

    private void actualizarT(int indice, int indiceComponente) {
        T2[indiceComponente].setText(String.format("%0" + (or(indiceComponente, 4, 8) ? 4 : 2) + "d", S.S[indice].getTiempo(indiceComponente - 1)));
    }

    private void seguridad() {

        boolean tiemposSeguros = seguridadJTextField(INT, true, 0, S.sise(), BAceptar, TIndice);
        tiemposSeguros = orFalse(tiemposSeguros, seguridadJTextField(new String[]{TIEMPO60, TIEMPO60, TIEMPO60, MSEGUNDOS, TIEMPO60, TIEMPO60, TIEMPO60, MSEGUNDOS}, BAceptar, Arrays.asList(T2).subList(1, T2.length).toArray(new JTextField[]{})));
        if (tiemposSeguros) {
            tiemposSeguros = seguridadLogica();
            actualizarLLogica(tiemposSeguros);
        }
        activarJComponent(tiemposSeguros, BAceptar);
        TIndice.setEnabled(!(RColocarAlFinal.isSelected() || RColocarAlComienzo.isSelected()));

    }

    private boolean seguridadLogica() {
        return getInferior().menorQue(getSuperior());
    }

    private void actualizarLLogica(boolean correcto) {
        LLogica.setText(correcto ? "" : "El tiempo final tiene que ser mayor que el inicial");
    }

