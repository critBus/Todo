    private void BAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BAceptarActionPerformed
        Seccion_De_Subtitulo s1 = S.crearSeccion(Integer.parseInt(T2[4].getText()), Integer.parseInt(T2[8].getText()), Integer.parseInt(T2[3].getText()), Integer.parseInt(T2[7].getText()), Integer.parseInt(T2[2].getText()), Integer.parseInt(T2[5].getText()), Integer.parseInt(T2[1].getText()), Integer.parseInt(T2[5].getText()), Ta.getText() + "\n");
        if (RSustituir.isSelected()) {
            S.añadirSeccion(s1, Integer.parseInt(TIndice.getText()) - 1);
        }
        if (RdesplazarElResto.isSelected()) {
            S.S = S.añadirUnElemento(Integer.parseInt(TIndice.getText()) - 1, s1);
        }
        if (RColocarAlFinal.isSelected()) {
            S.añadirSeccion(s1);
        }
        if (RColocarAlComienzo.isSelected()) {
            S.S = S.añadirUnElemento(0, s1);
        }
        S.ordernar();
        ventana_Principal.actualizarT();
        visualizarVentanPrincipal();     // TODO add your handling code here:
    }//GEN-LAST:event_BAceptarActionPerformed

